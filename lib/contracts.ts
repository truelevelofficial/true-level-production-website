import { contractTypes } from "./constants";
import { contractSchema } from "./validation";
import type { z } from "zod";

type ContractInput = z.infer<typeof contractSchema>;

export function contractTypeLabel(type: string) {
  return contractTypes.find(([value]) => value === type)?.[1] ?? "عقد خدمات";
}

const SEP = "\n========================================\n";

function clause(num: string, text: string) {
  return `${num} ${text}`;
}

function clientParty(data: ContractInput) {
  let p = `الطرف الثاني: ${data.clientName}`;
  if (data.clientCompanyName) p += `، بصفته ممثلا عن شركة/مؤسسة ${data.clientCompanyName}`;
  if (data.clientTaxId) p += `، الرقم القومي/البطاقة الضريبية: ${data.clientTaxId}`;
  if (data.clientAddress) p += `، العنوان: ${data.clientAddress}`;
  p += `، الهاتف: ${data.clientPhone}`;
  p += '، ويشار إليه لاحقا باسم "العميل".';
  return p;
}

