import { contractTypes } from "./constants";
import { contractSchema } from "./validation";
import type { z } from "zod";

type ContractInput = z.infer<typeof contractSchema>;

export function contractTypeLabel(type: string) {
  return contractTypes.find(([value]) => value === type)?.[1] ?? "عقد خدمات";
}

export { contentCreatorsDefaultClauses, contentCreatorsNdaDefaultClauses };

const SEP = "\n========================================\n";

function clause(num: string, text: string) {
  return `${num} ${text}`;
}

/* ===== CONTRACT METADATA ===== */
function contractMetaSection(data: Partial<ContractInput>) {
  const types = contractTypes as readonly (readonly [string, string])[];
  const typeLabel = types.find(([v]) => v === data.type)?.[1] || "عقد خدمات";
  return `${SEP}
بيانات العقد:

نوع العقد: ${typeLabel}
تاريخ العقد: ${data.projectStartDate}
رقم العقد: يُمنح تلقائيا بعد التثبيت`;
}

