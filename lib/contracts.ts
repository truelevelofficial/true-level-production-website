export function generateArabicContract(data: ContractInput) {
  const title = contractTypeLabel(data.type);

  let typeSections: string;
  switch (data.type) {
    case "STUDIO_RENTAL":
      typeSections = studioRentalSections(data);
      break;
    case "VIDEO_PRODUCTION":
      typeSections = videoProductionSections(data);
      break;
    case "UGC_CREATOR_CAMPAIGN":
      typeSections = ugcCampaignSections(data);
      break;
    case "EVENT_COVERAGE":
      typeSections = eventCoverageSections(data);
      break;
    case "MONTHLY_CONTENT_MANAGEMENT":
      typeSections = monthlyContentSections(data);
      break;
    default:
      typeSections = generalServiceSections(data);
  }

  const commonSections = [
    contractMetaSection(data),
    partySection(data),
    preamble(data),
    definitionSection(),
    scopeAgreementSection(),
    startExecutionSection(),
    typeSections,
    deliverySection(data),
    paymentSection(data),
    revisionsSection(data),
    contentApprovalSection(),
    ipSection(data),
    obligationsFirstParty(),
    obligationsSecondParty(),
    cancellationSection(data),
    latePaymentSection(data),
    confidentialitySection(data),
    forceMajeureSection(),
    liabilityLimitationSection(),
    noGuaranteeSection(),
    noticesSection(),
    governingLawSection(),
    generalProvisionsSection(),
    additionalNotes(data),
    signaturesSection(data),
  ].join("\n");

  return {
    title: `${title} - ${data.clientName}`,
    body: `مسودة عقد ${title}
${disclaimer()}
${commonSections}
`.trim(),
  };
}

/* ===== NUMBER TO ARABIC WORDS ===== */
function numberToArabicWords(num: number): string {
  if (num === 0) return "صفر";
  const units = ["", "واحد", "اثنان", "ثلاثة", "أربعة", "خمسة", "ستة", "سبعة", "ثمانية", "تسعة"];
  const tens = ["", "عشرة", "عشرون", "ثلاثون", "أربعون", "خمسون", "ستون", "سبعون", "ثمانون", "تسعون"];
  const hundreds = ["", "مائة", "مائتان", "ثلاثمائة", "أربعمائة", "خمسمائة", "ستمائة", "سبعمائة", "ثمانمائة", "تسعمائة"];

  function belowThousand(n: number): string {
    if (n === 0) return "";
    const h = Math.floor(n / 100);
    const remainder = n % 100;
    let result = h > 0 ? hundreds[h] : "";
    if (remainder > 0) {
      if (result) result += " و";
      if (remainder < 10) result += units[remainder];
      else if (remainder < 20) result += remainder === 10 ? "عشرة" : `${units[remainder % 10]} عشر`;
      else {
        const t = Math.floor(remainder / 10);
        const u = remainder % 10;
        result += u > 0 ? `${units[u]} و${tens[t]}` : tens[t];
      }
    }
    return result;
  }

  let result = "";
  if (num >= 1000000) {
    const millions = Math.floor(num / 1000000);
    result += millions === 1 ? "مليون" : `${belowThousand(millions)} مليون`;
    num %= 1000000;
    if (num > 0) result += " و";
  }
  if (num >= 1000) {
    const thousands = Math.floor(num / 1000);
    result += thousands === 1 ? "ألف" : `${belowThousand(thousands)} ألف`;
    num %= 1000;
    if (num > 0) result += " و";
  }
  if (num > 0) result += belowThousand(num);
  return result;
}
